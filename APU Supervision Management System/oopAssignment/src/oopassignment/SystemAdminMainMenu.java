    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(row == -1){
            JOptionPane.showMessageDialog(this, "Please select a row to reset password");
        }else{
            int option = JOptionPane.showConfirmDialog(this, "Are you sure you want to reset password for this account?\nThis action will reset the password of the account to a default password and cannot be undone", "Confirm Reset Password", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);
            if (option == JOptionPane.YES_OPTION){
                String defaultPassword = "password123";
                String id = accountList.get(row).getId();
                String role = accountList.get(row).getRole();
                
                updatePasswordInFile("Accounts.txt", id, defaultPassword);
                accountList.get(row).setAttempts("0");
                model.setValueAt("0", row, 2);
                
                
                if(role.equals("Student")){
                    updatePasswordInFile("students.txt", id, defaultPassword);
                } else if (role.equals("Supervisor")){
                    updatePasswordInFile("supervisor.txt", id, defaultPassword);
                } else if (role.equals("Faculty Admin")){
                    updatePasswordInFile("facultyAdmin.txt", id, defaultPassword);
                } else if (role.equals("System Admin")){
                    updatePasswordInFile("systemAdmin.txt", id, defaultPassword);
                }
                
                try{
                    FileReader fr = new FileReader("Accounts.txt");
                    BufferedReader br = new BufferedReader(fr);
                    ArrayList<String> accountList = new ArrayList<>();
                    String eachLine;
                    
                    while((eachLine = br.readLine()) != null){
                        if(eachLine.trim().isEmpty()){
                            continue;
                        }
                        String parts[] = eachLine.split(",");
                        if(parts[0].trim().equals(id.trim())){
                            if(parts.length >= 4){
                                parts[3] = "0";
                                eachLine = String.join(",", parts);
                            }else{
                                accountList.add(eachLine);
                                continue;
                            }
                        }
                        accountList.add(eachLine);
                    }
                    br.close();
                    fr.close();
                    
                    FileWriter fw = new FileWriter("Accounts.txt");
                    BufferedWriter bw = new BufferedWriter(fw);
                    
                    for (String line : accountList){
                        bw.write(line);
                        bw.newLine();
                    }
                    
                    bw.close();
                    fw.close();
                }catch(IOException e){
                    JOptionPane.showMessageDialog(this, "Error resetting attempts in Accounts.txt" + e.getMessage(), "File Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }
                
                JOptionPane.showMessageDialog(this, "The password for the account " + id + " has been reset to default ('password123')");
                jTable1.clearSelection();
                row = -1;
                
                
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

