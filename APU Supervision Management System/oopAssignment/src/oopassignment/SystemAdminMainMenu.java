    private void loadAccountsFromFile(){
        try{
            FileReader fr = new FileReader("Accounts.txt");
            BufferedReader br = new BufferedReader(fr);
            
            String eachLine;
            
            while((eachLine = br.readLine()) != null){
                if(eachLine.trim().isEmpty()){
                    continue;
                }
                
                String accountDetails[] = eachLine.split(",");
                List<String> account = new ArrayList<>();
                
                
                if(accountDetails.length < 4){
                    account.add(accountDetails[0]);
                    account.add(accountDetails[2]);
                    account.add("0");
                    Accounts a = new Accounts(accountDetails[0], accountDetails[2], "0");
                    accountList.add(a);
                    model.addRow(account.toArray());
                    continue;
                }
                
                account.add(accountDetails[0]);
                account.add(accountDetails[2]);
                account.add(accountDetails[3]);
                Accounts a = new Accounts(accountDetails[0], accountDetails[2], accountDetails[3]);
                accountList.add(a);
                model.addRow(account.toArray());
            }
            
            br.close();
            fr.close();
        }catch(IOException e){
            JOptionPane.showMessageDialog(this, "Error reading Accounts.txt" + e.getMessage(), "File Read Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    private void updatePasswordInFile(String fileName, String id, String password){
        try{
            FileReader fr = new FileReader(fileName);
            BufferedReader br = new BufferedReader(fr);
            ArrayList<String> updatedList = new ArrayList<>();
            String eachLine;
            
            while((eachLine = br.readLine()) != null){
                if(eachLine.trim().isEmpty()){
                    continue;
                }
                
                String parts[] = eachLine.split(",");
                if(parts[0].trim().equals(id.trim())){
                    int passwordIndex = fileName.equalsIgnoreCase("Accounts.txt") ? 1 : 2;
                    parts[passwordIndex] = password;
                    eachLine = String.join(",", parts);
                }
                updatedList.add(eachLine);
            }
            br.close();
            fr.close();
            
            FileWriter fw = new FileWriter(fileName);
            BufferedWriter bw = new BufferedWriter(fw);
            
            for (String line : updatedList){
                bw.write(line);
                bw.newLine();
            }
            
            bw.close();
            fw.close();
            
        }catch(IOException e){
            JOptionPane.showMessageDialog(this, "Error updating " + fileName +" "+ e.getMessage(), "File Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }

