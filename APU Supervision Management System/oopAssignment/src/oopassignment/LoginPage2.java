    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String userID = jTextField1.getText();
        String password = jTextField2.getText();
        
        try{
            FileReader fr = new FileReader("Accounts.txt");
            BufferedReader br = new BufferedReader(fr);
            String eachLine;
            
            boolean success = false;
            
            while((eachLine = br.readLine()) != null){
                String parts[] = eachLine.split(",");
                String accountID = parts[0].trim();
                String accountPassword = parts[1].trim();
                String role = parts[2].trim();
                
                int attempts = 0;
                
                if(!role.equalsIgnoreCase("System Admin")){
                    if(parts.length >= 4){
                        try{
                            attempts = Integer.parseInt(parts[3].trim());
                        }catch(NumberFormatException e){
                            attempts = 0;
                        }
                    }else{
                        attempts = 0;
                    }
                }
                
                
                if(userID.equals(accountID)){
                    String status = "";
                    success = true;
                    if(role.equals("Student")){
                        status = checkStatus("students.txt", accountID);
                    }else if(role.equals("Supervisor")){
                        status = checkStatus("supervisor.txt", accountID);
                    }else if(role.equals("Faculty Admin")){
                        status = checkStatus("facultyAdmin.txt", accountID);
                    }else if(role.equals("System Admin")){
                        status = checkStatus("systemAdmin.txt", accountID);
                    }  
                        
                    if((role.equalsIgnoreCase("System Admin") || attempts < 3) && (status.equalsIgnoreCase("active"))){
                        if(password.equals(accountPassword)){
                            resetAttempts(accountID);
                            
                            if(role.equals("Student")){
                                StudentMainPage smp = new StudentMainPage(userID);
                                smp.setVisible(true);
                                this.dispose();
                            }else if(role.equals("Supervisor")){
                                Mainpage m = new Mainpage(userID);
                                m.setVisible(true);
                                this.dispose();
                            }else if(role.equals("Faculty Admin")){
                                FacultyAdmin_FrameHome FA_frameHome = new FacultyAdmin_FrameHome();
                                FA_frameHome.setVisible(true);
                                FA_frameHome.setFacultyAdminID(userID);
                                this.dispose();
                            }else if(role.equals("System Admin")){
                                SystemAdminMainMenu mainMenu = new SystemAdminMainMenu();
                                mainMenu.setVisible(true);
                                this.dispose();
                            }  
                        }else{
                            addAttempts(accountID);
                            JOptionPane.showMessageDialog(this, "user ID or password is incorrect");
                        }
                    }else{
                        JOptionPane.showMessageDialog(this, "Your account is inactive or has too many attempts, please try again later");
                    }
                    break;
                }
                
            }    
            br.close();
            fr.close();
                
            if(!success){
                JOptionPane.showMessageDialog(this, "user ID or password is incorrect");
            }
            
        }catch(IOException e){
            JOptionPane.showMessageDialog(this, "Error reading Accounts.txt: " + e.getMessage(), "File Read Error", JOptionPane.ERROR_MESSAGE);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

