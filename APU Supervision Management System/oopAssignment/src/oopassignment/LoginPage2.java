    private String checkStatus(String fileName, String id){
        try{
            FileReader fr = new FileReader(fileName);
            BufferedReader br = new BufferedReader(fr);
            String eachLine;

            while((eachLine = br.readLine()) != null){
                String parts[] = eachLine.split(",");
                if (parts[0].equals(id)){
                    return parts[parts.length - 1];
                }
            }
        }catch(IOException e){
            JOptionPane.showMessageDialog(this, "Error Reading " + fileName);
        }
        return null;
    }
    
    private void addAttempts(String id){
        try{
            FileReader fr = new FileReader("Accounts.txt");
            BufferedReader br = new BufferedReader(fr);
            ArrayList<String> updatedList = new ArrayList<>();
            String eachLine;
            
            while((eachLine = br.readLine()) != null){
                String parts[] = eachLine.split(",");
                if (parts[0].trim().equals(id) && parts.length > 3){
                    int attempts = 0;
                    try{
                        attempts = Integer.parseInt(parts[3].trim());
                    }catch(NumberFormatException e){
                        attempts = 0;
                    }
                    parts[3] = String.valueOf(attempts + 1);
                    eachLine = String.join(",", parts);
                }
                updatedList.add(eachLine);
            }
            
            br.close();
            fr.close();
            
            FileWriter fw = new FileWriter("Accounts.txt");
            BufferedWriter bw = new BufferedWriter(fw);
            
            for (String line : updatedList){
                bw.write(line);
                bw.newLine();
            }
            
            bw.close();
            fw.close();
            
        }catch(IOException e){
            JOptionPane.showMessageDialog(this, "Error updating Accounts.txt");
        }   
    }
    
    private void resetAttempts(String id){
        try{
            FileReader fr = new FileReader("Accounts.txt");
            BufferedReader br = new BufferedReader(fr);
            
            ArrayList<String> updatedList = new ArrayList<>();
            String eachLine;
            
            while((eachLine = br.readLine()) != null){
                String parts[] = eachLine.split(",");
                if (parts[0].trim().equals(id) && parts.length > 3){
                    parts[3] = "0";
                    eachLine = String.join(",", parts);
                }
                updatedList.add(eachLine);
            }
            
            br.close();
            fr.close();
            
            FileWriter fw = new FileWriter("Accounts.txt");
            BufferedWriter bw = new BufferedWriter(fw);
            
            for (String line : updatedList){
                bw.write(line);
                bw.newLine();
            }
            
            bw.close();
            fw.close();
        }catch(IOException e){
            JOptionPane.showMessageDialog(this, "Error when reseting attempts in Accounts.txt: " + e.getMessage(), "File Read Writer Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
