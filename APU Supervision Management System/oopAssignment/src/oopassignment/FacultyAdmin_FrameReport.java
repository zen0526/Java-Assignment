    private void jButtonSupervisorFilterSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSupervisorFilterSearchActionPerformed
        //Display Original Table first to clear existing Filters
        FA_TableManager.displaySupervisorTable(modelSupervisor,supervisorList);
        //Create new table filter object
        supervisorFilter = new FA_TableFilter(modelSupervisor);
        String course = jTextFieldSupervisorFilterCourse.getText();

        //If Empty, clear table filter
        if (course.isEmpty()){
            FA_TableManager.displaySupervisorTable(modelSupervisor,supervisorList);
        }
        else{
            String[] filterValues = {course};
            int[] filterColumns = {2};
            supervisorFilter.applyFilters(filterValues,filterColumns);
        }
    }//GEN-LAST:event_jButtonSupervisorFilterSearchActionPerformed

    private void jButtonStudentFilterSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonStudentFilterSearchActionPerformed
        FA_TableManager.displayStudentTable(modelStudent,studentList);
        studentFilter = new FA_TableFilter(modelStudent);
        String intake = jTextFieldStudentFilterIntake.getText();
        String course = jTextFieldStudentFilterCourse.getText();

        if (intake.isEmpty() && course.isEmpty()){
            FA_TableManager.displayStudentTable(modelStudent,studentList);
        }
        else{
            String[] filterValues = {intake,course};
            int[] filterColumn = {2,3};
            studentFilter.applyFilters(filterValues,filterColumn);
        }
    }//GEN-LAST:event_jButtonStudentFilterSearchActionPerformed

