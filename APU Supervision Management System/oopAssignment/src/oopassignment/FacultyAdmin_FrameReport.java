    public final void setReportRelatedData(){
        
        for (Student student : studentList){
            
            //Skip student if they are Inactive
            if(student.getStatus().equals("Inactive")){
                continue;
            }
            String tpNum = student.gettpNum();
            String studentIntake = student.getintakecode();
            String studentCourse = student.getCourse();
            //Check if Intake List contains intakeCode of student
            boolean intakeAdded = false;
            for(String[] intake : intakeCodeList){
                if (intake[0].equals(studentIntake)){
                    intakeAdded = true;
                    break;
                }
            }
            //Add intakeCode to List if not yet added
            if (intakeAdded == false){
                intakeCodeList.add(new String[]{studentIntake,"0"});
            }
            
            //Add up numberOfStudents for each intake
            for (String[] intake : intakeCodeList){
                if(intake[0].equals(studentIntake)){
                    int numOfStudents = Integer.parseInt(intake[1]);
                    numOfStudents++;
                    intake[1] = Integer.toString(numOfStudents);
                }
            }
            //Check if course is added in list
            boolean courseAdded = false;
            for (String[] course : courseList){
                if (course[0].equals(studentCourse)){
                    courseAdded = true;
                    break;
                }
            }
            //Add course to list if not yet added
            if (courseAdded == false){
                courseList.add(new String[]{studentCourse,"0"});
            }
            
            //Add up numberOfStudents for each Course
            for (String[] course : courseList){
                
                if(course[0].equals(studentCourse)){
                    int numOfStudents = Integer.parseInt(course[1]);
                    numOfStudents++;
                    course[1] = Integer.toString(numOfStudents);
                }
            }
            
            totalActiveStudentCount++;
        }
        
        for (Supervisor supervisor : supervisorList){
            assignedStudentCount += Integer.parseInt(supervisor.getnumberofStudent());
            String supervisorDepartment = supervisor.getDepartment();
            
            //Check if additional course is added in list
            boolean courseAdded = false;
            for (String[] course : courseList){
                if (course[0].equals(supervisorDepartment)){
                    courseAdded = true;
                    break;
                }
            }
            //Add course to list if not yet added
            if (courseAdded == false){
                courseList.add(new String[]{supervisorDepartment,"0"});
            }
            totalActiveSupervisorCount++;
        }
        
        
    }
    

    public StringBuilder generateReportStudentPerIntake(StringBuilder report){
        
        for(String[] intake : intakeCodeList){
            report.append(String.format("""
                                        Intake Code: %s
                                        
                                        Total Number of Students: %s
                                        
                                        Courses:
                                        """,intake[0],intake[1]));
            for(String[] course : courseList){
                int intakecourseNumOfStudents = 0;
                for(Student student : studentList){
                    if(student.getintakecode().equals(intake[0]) && student.getCourse().equals(course[0])){
                        intakecourseNumOfStudents++;
                    }
                }
                report.append(String.format("""
                                                - %s (%d Students)
                                            """,course[0],intakecourseNumOfStudents));
            }
            report.append(String.format("""
                                        
                                        ---------------------------------------------------------------------
                                        """));
            
        }
        report.append(String.format("""
                                    ========================================
                                     End Of Report
                                    ========================================
                                    """));
        
        return report;
    }
    
    public StringBuilder generateReportStudentPerSupervisor(StringBuilder report){
        for (String[] course : courseList){
            boolean supervisorBelongstoCurrentCourse = false;
            report.append(String.format("""
                                        ========================================
                                        ----- Course: %s -----
                                        """,course[0]));
            for (Supervisor supervisor : supervisorList){
                if (!(supervisor.getDepartment().equals(course[0]))){
                    continue;
                }
                else{
                    supervisorBelongstoCurrentCourse = true;
                }
                
                
                String supervisorID = supervisor.getsupervisorID();
                String supervisorName = supervisor.getName();
                String supervisorNumOfStudents = supervisor.getnumberofStudent();
                
                report.append(String.format("""
                                            ---------------------------------------------------------------------
                                            Supervisor: %s, %s
                                            Assigned Students: %s
                                            
                                            """,supervisorID,supervisorName,supervisorNumOfStudents));
                
                //Get supervisor assigned students
                ArrayList<String[]> assignedStudents = new ArrayList<>();
                ArrayList<String> assignedIntakes = new ArrayList<>();
                
                for (Assignment data : assignmentList){
                    if (data.getSupervisorID().equals(supervisorID)){
                        assignedStudents.add(new String[]{data.getTpNum(),data.getStudentName(),data.getIntakeCode()});
                        if (!assignedIntakes.contains(data.getIntakeCode())){
                            assignedIntakes.add(data.getIntakeCode());
                        }
                    }     
                }
                
                Collections.sort(assignedIntakes);
                for (String intakeCode : assignedIntakes){
                    report.append(intakeCode+"\n");
                    
                    for (String[] data : assignedStudents){
                        if (data[2].equals(intakeCode)){
                            report.append(String.format("""
                                                        - %s, %s
                                                        """,data[0],data[1]));
                        }
                    }
                    report.append("\n");
                }
            }
            if (supervisorBelongstoCurrentCourse == false){
                    report.append("""
                                  ---------------------------------------------------------------------
                                  No Supervisor in the Course
                                  
                                  """);
            }
        }
        report.append(String.format("""
                                    ========================================
                                     End Of Report
                                    ========================================
                                    """));
        
        return report;
    }
    
