    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        String assignID = jTextFieldAssignmentID.getText().trim();
        String supervisorID = jTextFieldSupervisorID.getText().trim();
        String tpNum = jTextFieldStudentID.getText().trim();
        String course = jTextFieldCourse.getText().trim();
        
        FA_AssignmentValidation validationEdit = new FA_AssignmentValidation(assignID,tpNum,supervisorID,course,studentList,supervisorList,assignmentList);
        StringBuilder errorMessage = new StringBuilder("❌ Invalid Input! Following Issues...\n");
        validationEdit.setEditFlag(true);
        validationEdit.setAssignIDBeforeEdit(assignIDBeforeEdit);
        
        boolean validInput = true;
        if (validationEdit.ensureNoEmptyInput()==false){
            errorMessage.append("There are empty input Fields.\n");
            validInput = false;
        }
        if (validationEdit.ensureInputExists()==false){
            errorMessage.append("Entered StudentID/SupervisorID doesn't exists, course doesn't belong to student or Not in correct Format.\n");
            validInput = false;
        }
        if (validationEdit.ensureStudentSupervisorSameDepartment()==false){
            errorMessage.append("Entered StudentID & SupervisorID are not under the same Department/Course.\n");
            validInput = false;
        }
        if (validationEdit.ensureStudentSupervisorActive()==false){
            errorMessage.append("Entered StudentID/SupervisorID is not Active.\n");
            validInput = false;
        }
        if (validationEdit.ensureNoDuplicatedStudent()==false){
            errorMessage.append("Entered StudentID is already Assigned.\n");
            validInput = false;
        }
        if (validationEdit.ensureNoDuplicatedAssignID()==false){
            errorMessage.append("Entered AssignID already exists.\n");
            validInput = false;
        }
        if (validationEdit.ensureCorrectAssignIDFormat()==false){
            errorMessage.append("Entered AssigniD must be in \"A123\" format.\n");
            validInput = false;
        }
        
        if (validInput == true){
            int choice = JOptionPane.showConfirmDialog(this, "Are you sure you want to Edit or Reassign?");
            if (choice==0){
                //Set Course to correct capitalization
                course = validationEdit.getStudentCourse();
                //Assign Values to Assignment Table
                validationEdit.setAssignmentStatus("Active");
                validationEdit.deriveSupervisorStudentInfo();
                String supervisorName = validationEdit.getSupervisorName();
                String studentName = validationEdit.getStudentName();
                String studentIntake = validationEdit.getStudentIntake();
                String assignmentStatus = validationEdit.getAssignmentStatus();

                //Replace original data with new data
                for (Assignment data : assignmentList){
                    if (data.getAssignID().equals(assignIDBeforeEdit)){
                        data.setAssignID(assignID);
                        data.setSupervisorID(supervisorID);
                        data.setSupervisorName(supervisorName);
                        data.setTpNum(tpNum);
                        data.setStudentName(studentName);
                        data.setIntakeCode(studentIntake);
                        data.setCourse(course);
                        data.setAssignStatus(assignmentStatus);
                    }
                }

                //Find which Row is Selected, then modify row data
                //Used tempRow instead of row (because row value may not belong to the actual selected row after filtering table)
                for (int tempRow = 0; tempRow < modelAssignment.getRowCount(); tempRow++) {
                    String currentAssignID = modelAssignment.getValueAt(tempRow, 0).toString(); // Assume ID is in column 0
                    if (currentAssignID.equals(assignIDBeforeEdit)) {
                        // Found the matching tempRow, now update other columns
                        modelAssignment.setValueAt(assignID, tempRow, 0);
                        modelAssignment.setValueAt(supervisorID, tempRow, 1);
                        modelAssignment.setValueAt(supervisorName, tempRow, 2);
                        modelAssignment.setValueAt(tpNum, tempRow, 3);
                        modelAssignment.setValueAt(studentName, tempRow, 4);
                        modelAssignment.setValueAt(studentIntake, tempRow, 5);
                        modelAssignment.setValueAt(course, tempRow, 6);
                        modelAssignment.setValueAt(assignmentStatus, tempRow, 7);
                        break; 
                    }
                }



                //append new data tempRow to assignment.txt
                FA_FileHandling.writeAssignmentFile("assignment.txt", assignmentList);

                //Counnting and Updating Number of Students for each Supervisor
                for (Supervisor supervisor : supervisorList){
                    if (supervisor.getsupervisorID().equals(supervisorID)){
                        supervisor.countSupervisorNumberOfStudent(assignmentList);
                    }
                }
                FA_FileHandling.writeSupervisorFile("supervisor.txt",supervisorList);

                FA_TableManager.displaySupervisorTable(modelSupervisor, supervisorList);



                // Set text fields to empty after adding valid data
                jTextFieldAssignmentID.setText("");
                jTextFieldSupervisorID.setText("");
                jTextFieldStudentID.setText("");
                jTextFieldCourse.setText("");
            }
        }
        else{
            JOptionPane.showMessageDialog(this,errorMessage);
        }
    }//GEN-LAST:event_jButtonEditActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        
        String assignID = jTextFieldAssignmentID.getText().trim();
        String supervisorID = jTextFieldSupervisorID.getText().trim();
        String tpNum = jTextFieldStudentID.getText().trim();
        String course = jTextFieldCourse.getText().trim();
        
        int choice = JOptionPane.showConfirmDialog(this,"Are you sure you want to Delete selected Data:\n  "+assignID+", "+supervisorID+", "+tpNum+", "+course);
        if (choice == 0){
            for (Assignment data : assignmentList){
                if (data.getAssignID().equals(assignID)){
                    assignmentList.remove(data);     
                    break;
                }
            }
            FA_FileHandling.writeAssignmentFile("assignment.txt", assignmentList);
                    
            for(Supervisor supervisor : supervisorList){
                if (supervisor.getsupervisorID().equals(supervisorID)){
                    supervisor.countSupervisorNumberOfStudent(assignmentList);
                    break;
                }
            }
            FA_FileHandling.writeSupervisorFile("supervisor.txt",supervisorList);

            //Update new Table values after deleting tempRow
            for (int tempRow = 0; tempRow < modelAssignment.getRowCount(); tempRow++) {
                String currentAssignID = modelAssignment.getValueAt(tempRow, 0).toString(); // Assume ID is in column 0
                if (currentAssignID.equals(assignIDBeforeEdit)) {
                    modelAssignment.removeRow(tempRow);
                }
            }
            
            FA_TableManager.displaySupervisorTable(modelSupervisor,supervisorList);

            jTextFieldAssignmentID.setText("");
            jTextFieldSupervisorID.setText("");
            jTextFieldStudentID.setText("");
            jTextFieldCourse.setText("");
        }
        
        
    }//GEN-LAST:event_jButtonDeleteActionPerformed


    
