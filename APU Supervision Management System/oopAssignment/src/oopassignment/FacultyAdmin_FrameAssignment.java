    private void jButtonAssignActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAssignActionPerformed
        String assignID = jTextFieldAssignmentID.getText().trim();
        String supervisorID = jTextFieldSupervisorID.getText().trim();
        String studentID = jTextFieldStudentID.getText().trim();
        String course = jTextFieldCourse.getText().trim();
        
        //Validation object created for validating User Inputs
        FA_AssignmentValidation validation1 = new FA_AssignmentValidation(assignID,studentID,supervisorID,course,studentList,supervisorList,assignmentList);
        StringBuilder errorMessage = new StringBuilder("❌ Invalid Input! Following Issues...\n");
        validation1.setEditFlag(false);
        
        boolean validInput = true;
        if (validation1.ensureNoEmptyInput()==false){
            errorMessage.append("There are empty input Fields.\n");
            validInput = false;
        }
        if (validation1.ensureInputExists()==false){
            errorMessage.append("Entered StudentID/SupervisorID doesn't exists, course doesn't belong to student or Not in correct Format.\n");
            validInput = false;
        }
        if (validation1.ensureStudentSupervisorSameDepartment()==false){
            errorMessage.append("Entered StudentID & SupervisorID are not under the same Department/Course.\n");
            validInput = false;
        }
        if (validation1.ensureStudentSupervisorActive()==false){
            errorMessage.append("Entered StudentID/SupervisorID is not Active.\n");
            validInput = false;
        }
        if (validation1.ensureNoDuplicatedStudent()==false){
            errorMessage.append("Entered StudentID is already Assigned.\n");
            validInput = false;
        }
        if (validation1.ensureNoDuplicatedAssignID()==false){
            errorMessage.append("Entered AssignID already exists.\n");
            validInput = false;
        }
        if (validation1.ensureCorrectAssignIDFormat()==false){
            errorMessage.append("Entered AssigniD must be in \"A123\" format.\n");
            validInput = false;
        }
        
        //When User Inputs are Valid
        if (validInput == true){
            //Set Course to correct capitalization
            course = validation1.getStudentCourse();
            //Assign Derived Values to Assignment Table
            validation1.setAssignmentStatus("Active");
            validation1.deriveSupervisorStudentInfo();
            String supervisorName = validation1.getSupervisorName();
            String studentName = validation1.getStudentName();
            String studentIntake = validation1.getStudentIntake();
            String assignmentStatus = validation1.getAssignmentStatus();
            
            //Add new Assignment Object
            assignmentList.add(new Assignment(assignID,supervisorID,supervisorName,studentID,studentName,studentIntake,course,assignmentStatus));
            //Add new Row to Assignment table
            modelAssignment.addRow(assignmentList.get(assignmentList.size()-1).getAssignmentDataList());
            
            //Append new dataLine in assignment.txt
            ArrayList<String[]> newDataLine = new ArrayList<>();
            newDataLine.add(assignmentList.get(assignmentList.size()-1).getAssignmentDataList());
            FA_FileHandling.writeFile("assignment.txt", newDataLine, true);
            
            //Update Supervisor's number of students
            for (Supervisor supervisor : supervisorList){
                if (supervisor.getsupervisorID().equals(supervisorID)){
                    supervisor.countSupervisorNumberOfStudent(assignmentList);
                }
            }
            FA_FileHandling.writeSupervisorFile("supervisor.txt",supervisorList);
            FA_TableManager.displaySupervisorTable(modelSupervisor, supervisorList);
            
            // Set text fields to empty after adding valid data
            jTextFieldAssignmentID.setText("");
            jTextFieldSupervisorID.setText("");
            jTextFieldStudentID.setText("");
            jTextFieldCourse.setText("");
            
        }
        else{
            //Show error message if Invalid Input
            JOptionPane.showMessageDialog(this,errorMessage);
        }
        
        
        
        
        
        
        //Check AssignID already exist
        //Check SupervisorID exist
        //Check StudentID exist
        //Check if Intake & Course match with StudentID
        
    }//GEN-LAST:event_jButtonAssignActionPerformed

